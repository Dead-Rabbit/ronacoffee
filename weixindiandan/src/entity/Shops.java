package entity;

public class Shops {
	private String shopId = "";
    private String shopName = "";
    private String shopPosition_longitude = "";
    private String shopPosition_lat = "";
	public String getShopId() {
		return shopId;
	}
	public void setShopId(String shopId) {
		this.shopId = shopId;
	}
	public String getShopName() {
		return shopName;
	}
	public void setShopName(String shopName) {
		this.shopName = shopName;
	}
	public String getShopPosition_longitude() {
		return shopPosition_longitude;
	}
	public void setShopPosition_longitude(String shopPosition_longitude) {
		this.shopPosition_longitude = shopPosition_longitude;
	}
	public String getShopPosition_lat() {
		return shopPosition_lat;
	}
	public void setShopPosition_lat(String shopPosition_lat) {
		this.shopPosition_lat = shopPosition_lat;
	}

    
}
